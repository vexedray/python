# 2 - Verificar dia
                
# Váriaveis

dia = int (input ("Digite um número de um a sete: "))

# Operadores

if dia == 1:
    print ("É domingo")
else:
    if dia == 2:
        print ("É segunda")
    else:
        if dia == 3:
            print ("É terça")
        else:
            if dia == 4:
                print ("É quarta")
            else:
                if dia == 5:
                    print ("É quinta")
                else:
                    if dia == 6:
                        print ("É sexta")
                    else:
                        if dia == 7:
                            print ("É sábado")
                        else:
                            print ("Número inválido")
